// Dictionary/Map สำหรับค้นหาครูรายคนด้วย id แบบ O(1) แทนการ .find() วน Array
// ใช้ตอนต้องการข้อมูลของครู "คนเดียว" เช่น หน้ารายละเอียด, ลิงก์อ้างอิงจากที่อื่น
export const teachersLookup: Record<string, Teacher> = teachersData.reduce(
    (acc, teacher) => {
        acc[teacher.id] = teacher;
        return acc;
    },
    {} as Record<string, Teacher>
);

// จัดกลุ่มครูตามอาคารที่ประจำ (locationId -> Teacher[]) คำนวณครั้งเดียวตอน import
// ใช้แทน teachersData.filter(t => t.locationIds.includes(locId)) ที่ต้องวนครูทั้งหมดทุกครั้ง
// (เช่นหน้า LocationDetailPage ที่เปิดดูทีละอาคาร จะได้ O(1) แทน O(n))
export const teachersByLocation: Record<string, Teacher[]> = teachersData.reduce(
    (acc, teacher) => {
        (teacher.locationIds || []).forEach((locId) => {
            const key = String(locId);
            if (!acc[key]) acc[key] = [];
            acc[key].push(teacher);
        });
        return acc;
    },
    {} as Record<string, Teacher[]>
);